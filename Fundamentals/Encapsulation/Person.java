package Fundamentals.Encapsulation;

public class Person {
    private String name;
    private int age;



    // Getter method
    public String getName() {
        return name;
    }
    // Setter method
    public void setName(String name) {
        this.name = name;
    }
    public int getAge() {
        return age;
    }
    public void setAge(int age) {
        this.age = age;
    }

    
}
