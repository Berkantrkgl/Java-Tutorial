package Fundamentals.Syntax;
/*
 * In Java, class names must start with a capital letter.
 *  BasicSyntax and basixSyntax have different meanings in java. 
 * In addition, the file name and the class name must be exactly
 *  the same.
 */
public class BasicSyntax {

/*
 * Every java program must contain a main method. 
 * Below you can see the syntax of the main method.
 */
    public static void main(String[] args) { 
        /*
         * The curly braces {} 
         * marks the beginning and the end of a block of code.
         */
        System.out.println("Hello World !");
        /*
         * system is a java class with useful functions like
         *  out which is short for output. println is 
         * short for "print line". These are used to 
         * print to the Java screen.
         */
    }
}
